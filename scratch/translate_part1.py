def main():
    if not os.path.exists(DATA_FILE):
        print(f"Error: {DATA_FILE} not found.")
        return

    with open(DATA_FILE, 'r', encoding='utf-8') as f:
        articles = json.load(f)

    updated_count = 0
    for art in articles:
        pid = art['id']
        if pid in TRANSLATIONS_PART1:
            trans = TRANSLATIONS_PART1[pid]
            art['title'] = trans['title']
            art['category'] = trans['category']
            art['methodology'] = trans['methodology']
            art['summary'] = trans['summary']
            art['clinicalImplication'] = trans['clinicalImplication']
            art['researchImplication'] = trans['researchImplication']
            updated_count += 1

    with open(DATA_FILE, 'w', encoding='utf-8') as f:
        json.dump(articles, f, ensure_ascii=False, indent=2)

    print(f"Successfully translated {updated_count} articles (Part 1).")

if __name__ == "__main__":
    main()
